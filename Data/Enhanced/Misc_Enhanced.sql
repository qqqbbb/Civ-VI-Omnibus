--Increase Barbarian XP
UPDATE GlobalParameters
	SET Value = 2
	WHERE Name = 'EXPERIENCE_BARB_SOFT_CAP';

UPDATE GlobalParameters
	SET Value = 3
	WHERE Name = 'EXPERIENCE_MAX_BARB_LEVEL';

--Increase counterspy mission length
UPDATE UnitOperations
	SET Turns = 64
	WHERE OperationType = 'UNITOPERATION_SPY_COUNTERSPY';
	
--Delete preferred agendas
DELETE FROM AgendaPreferredLeaders;

--Add population requirement for unique districts
UPDATE Districts
	SET RequiresPopulation = 1
	WHERE DistrictType = 'DISTRICT_ACROPOLIS'
		OR DistrictType = 'DISTRICT_HANSA'
		OR DistrictType = 'DISTRICT_LAVRA'
		OR DistrictType = 'DISTRICT_ROYAL_NAVY_DOCKYARD'
		OR DistrictType = 'DISTRICT_STREET_CARNIVAL';

--Unlock Forts at Military Engineering
UPDATE Improvements
	SET PrereqTech = 'TECH_MILITARY_ENGINEERING'
	WHERE ImprovementType = 'IMPROVEMENT_FORT';
	
